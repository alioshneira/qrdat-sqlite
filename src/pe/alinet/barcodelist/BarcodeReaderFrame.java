    @Override
    public void run() {
        do {
            try {
                    Thread.sleep(100);
            } catch (InterruptedException e) {
                    e.printStackTrace();
            }
            
            BufferedImage image = null;
            
            
            if (webcam.isOpen()) {

                if ((image = webcam.getImage()) == null) {
                        continue;
                }
                
                LuminanceSource source = new BufferedImageLuminanceSource(image);
                BinaryBitmap bitmap = new BinaryBitmap(new HybridBinarizer(source));
                Result result = null;
                String codigo = null;
                try {
                    result = new MultiFormatReader().decode(bitmap);
                    codigo = result.getText();
                    procesar(codigo);
                    
                } catch (NotFoundException e) {
                        // fall thru, it means there is no QR code in image
                    //Logger.getLogger(QRScannerFrame.class.getName()).log(Level.FINEST, null, e);
                }

                if (codigo != null){

                    //this.lbOut.setText(qrscanner.getText());
                    
                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException e) {
                        e.printStackTrace();
                    }
                }
            }
            
        }
        while(true);

    }

    @Override
    public Thread newThread(Runnable r) {
        Thread t = new Thread(r, "barcode-reader-panel");
        t.setDaemon(true);
        return t;
    }
    
    private void procesar(String codigo){
        
        Usuario u = UsuarioService.getById(codigo);
        if (u != null){
            BarcodeListService.add(u);
            this.lbMensaje.setText("C贸digo: "+codigo);
            this.lbMensaje.setForeground(Color.blue);
        }
        else {
            this.lbMensaje.setText("No se encontr贸 el usuario con el c贸digo: "+codigo);
            this.lbMensaje.setForeground(Color.red);
        }
        
        Toolkit.getDefaultToolkit().beep();

        this.txtCodigo.setText("");
        refreshTable();
    }
